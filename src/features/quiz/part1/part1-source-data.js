require("./types");

function optionId(questionId, index) {
  return `${questionId}_OPT_${String.fromCharCode(65 + index)}`;
}

function pool(id, targetLesson, skill, knowledgeTarget, grammarUsed, questionDefs) {
  return {
    id,
    targetLesson,
    skill,
    knowledgeTarget,
    questions: questionDefs.map((definition, index) => {
      const questionId = `${id}_Q${String(index + 1).padStart(3, "0")}`;
      const [stem, correctValue, distractors, explanationVi, vocabularyUsed = []] = definition;
      const optionTexts = [correctValue, ...distractors];
      const options = optionTexts.map((text, optionIndex) => ({
        id: optionId(questionId, optionIndex),
        text,
      }));

      return {
        id: questionId,
        poolId: id,
        targetLesson,
        skill,
        knowledgeTarget,
        difficulty: skill.includes("dialogue") || skill === "dialogue" ? "medium" : "easy",
        stem,
        options,
        correctOptionId: options[0].id,
        correctValue,
        explanationVi,
        grammarUsed,
        vocabularyUsed,
        validation: {
          grammarInScope: true,
          vocabularyInScope: true,
          singleCorrectAnswer: true,
          reviewed: true,
        },
      };
    }),
  };
}

